CREATE TABLE ACCOUNTS(
    AID varchar(6) PRIMARY KEY,
    Afname varchar(20) NOT NULL,
    Alname varchar(20) NOT NULL,
    Email varchar(30) NOT NULL,
    NumberOfUsers int DEFAULT 1
);

CREATE TABLE Acc_Phone(
    AID varchar(6) NOT NULL,
    Phone varchar(11) UNIQUE NOT NULL,
    PRIMARY KEY(AID,Phone),
    FOREIGN KEY (AID) REFERENCES ACCOUNTS(AID)
);

CREATE TABLE USERS(
    UID varchar(6) PRIMARY KEY,
    Ufname varchar(20) NOT NULL,
    Ulname varchar(20) NOT NULL,
    AID varchar(6) NOT NULL,
    FOREIGN KEY (AID) REFERENCES ACCOUNTS(AID)
);

CREATE TABLE SUBSCRIPTIONS(
    SID varchar(6) PRIMARY KEY,
    Stype varchar(20) NOT NULL,
    PaymentMethod varchar(15) NOT NULL,
    DOS DATE NOT NULL,
    AID varchar(6) NOT NULL,
    FOREIGN KEY (AID) REFERENCES ACCOUNTS(AID)
);

CREATE TABLE LIVES(
    LID varchar(6) PRIMARY KEY,
    Lname varchar(20) NOT NULL,
    DOL DATE NOT NULL,
    Lcategory varchar(20) NOT NULL,
    UID varchar(6) NOT NULL,
    FOREIGN KEY (UID) REFERENCES USERS(UID)
);

CREATE TABLE PODCASTS(
    POID varchar(7) PRIMARY KEY,
    POname varchar(20) NOT NULL,
    POduration int NOT NULL,
    POcategory varchar(20) NOT NULL,
    UID varchar(6) NOT NULL,
    FOREIGN KEY (UID) REFERENCES USERS(UID)
);

CREATE TABLE PLAYLISTS(
    PID varchar(6) PRIMARY KEY,
    Pname varchar(20) NOT NULL,
    DOC DATE NOT NULL,
    UID varchar(6) NOT NULL,
    FOREIGN KEY (UID) REFERENCES USERS(UID)
);

CREATE TABLE ARTISTS(
    ARID varchar(7) PRIMARY KEY,
    ARfname varchar(20) NOT NULL,
    ARlname varchar(20) NOT NULL,
    DOB DATE NOT NULL
);

CREATE TABLE BANDS(
    BID varchar(6) PRIMARY KEY,
    Bname varchar(20) NOT NULL,
    DOC DATE NOT NULL
);

CREATE TABLE BAND_MEMBERS(
    BID varchar(6) NOT NULL,
    BMID varchar(7) NOT NULL,
    BMfname varchar(20) NOT NULL,
    BMlname varchar(20) NOT NULL,
    BMjob varchar(20) NOT NULL,
    FOREIGN KEY (BID) REFERENCES BANDS(BID)
);

CREATE TABLE ALBUMS(
    ALID varchar(7) PRIMARY KEY,
    ALname varchar(20) NOT NULL,
    DOR DATE NOT NULL,
    ARID varchar(7),
    BID varchar(6),
    FOREIGN KEY (ARID) REFERENCES ARTISTS(ARID),
    FOREIGN KEY (BID) REFERENCES BANDS(BID)
);

CREATE TABLE SONGS(
    SOID varchar(7) PRIMARY KEY,
    SOname varchar(20) NOT NULL,
    SOtype varchar(20) NOT NULL,
    DOR DATE NOT NULL,
    SOduration int NOT NULL,
    ALID varchar(7) NOT NULL,
    FOREIGN KEY (ALID) REFERENCES ALBUMS(ALID)
);

CREATE TABLE USERS_WATCHES_LIVES(
    UID varchar(6) NOT NULL,
    LID varchar(6) NOT NULL,
    PRIMARY KEY(UID,LID),
    FOREIGN KEY (UID) REFERENCES USERS(UID),
    FOREIGN KEY (LID) REFERENCES LIVES(LID)
);

CREATE TABLE USERS_LISTENS_PODCASTS(
    UID varchar(6) NOT NULL,
    POID varchar(7) NOT NULL,
    PRIMARY KEY(UID,POID),
    FOREIGN KEY (UID) REFERENCES USERS(UID),
    FOREIGN KEY (POID) REFERENCES PODCASTS(POID)
);

CREATE TABLE USERS_LIKES_SONGS(
    UID varchar(6) NOT NULL,
    SOID varchar(7) NOT NULL,
    PRIMARY KEY(UID,SOID),
    FOREIGN KEY (UID) REFERENCES USERS(UID),
    FOREIGN KEY (SOID) REFERENCES SONGS(SOID)
);

CREATE TABLE USERS_DOWNLOADS_SONGS(
    UID varchar(6) NOT NULL,
    SOID varchar(7) NOT NULL,
    PRIMARY KEY(UID,SOID),
    FOREIGN KEY (UID) REFERENCES USERS(UID),
    FOREIGN KEY (SOID) REFERENCES SONGS(SOID)
);

CREATE TABLE USERS_FOLLOWS_ARTISTS(
    UID varchar(6) NOT NULL,
    ARID varchar(7) NOT NULL,
    PRIMARY KEY(UID,ARID),
    FOREIGN KEY (UID) REFERENCES USERS(UID),
    FOREIGN KEY (ARID) REFERENCES ARTISTS(ARID)
);

CREATE TABLE USERS_FOLLOWS_BANDS(
    UID varchar(6) NOT NULL,
    BID varchar(6) NOT NULL,
    PRIMARY KEY(UID,BID),
    FOREIGN KEY (UID) REFERENCES USERS(UID),
    FOREIGN KEY (BID) REFERENCES BANDS(BID)
);

CREATE TABLE PLAYLISTS_CONTAINS_SONGS(
    PID varchar(6) NOT NULL,
    SOID varchar(7) NOT NULL,
    PRIMARY KEY(PID,SOID),
    FOREIGN KEY (PID) REFERENCES PLAYLISTS(PID),
    FOREIGN KEY (SOID) REFERENCES SONGS(SOID)
);

